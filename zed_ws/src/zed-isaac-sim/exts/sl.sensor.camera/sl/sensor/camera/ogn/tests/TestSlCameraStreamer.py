import omni.kit.test
import omni.graph.core as og
import omni.graph.core.tests as ogts
import os


class TestOgn(ogts.OmniGraphTestCase):

    async def test_data_access(self):
        from sl.sensor.camera.ogn.SlCameraStreamerDatabase import SlCameraStreamerDatabase
        test_file_name = "SlCameraStreamerTemplate.usda"
        usd_path = os.path.join(os.path.dirname(__file__), "usd", test_file_name)
        if not os.path.exists(usd_path):
            self.assertTrue(False, f"{usd_path} not found for loading test")
        (result, error) = await ogts.load_test_file(usd_path)
        self.assertTrue(result, f'{error} on {usd_path}')
        test_node = og.Controller.node("/TestGraph/Template_sl_sensor_camera_ZED_Camera")
        database = SlCameraStreamerDatabase(test_node)
        self.assertTrue(test_node.is_valid())
        node_type_name = test_node.get_type_name()
        self.assertEqual(og.GraphRegistry().get_node_type_version(node_type_name), 1)

        def _attr_error(attribute: og.Attribute, usd_test: bool) -> str:
            test_type = "USD Load" if usd_test else "Database Access"
            return f"{node_type_name} {test_type} Test - {attribute.get_name()} value error"


        self.assertTrue(test_node.get_attribute_exists("inputs:camera_prim"))
        attribute = test_node.get_attribute("inputs:camera_prim")
        db_value = database.inputs.camera_prim

        self.assertTrue(test_node.get_attribute_exists("inputs:exec_in"))
        attribute = test_node.get_attribute("inputs:exec_in")
        db_value = database.inputs.exec_in
        expected_value = 0
        actual_value = og.Controller.get(attribute)
        ogts.verify_values(expected_value, actual_value, _attr_error(attribute, True))
        ogts.verify_values(expected_value, db_value, _attr_error(attribute, False))

        self.assertTrue(test_node.get_attribute_exists("inputs:serial_number"))
        attribute = test_node.get_attribute("inputs:serial_number")
        db_value = database.inputs.serial_number
        expected_value = 20976320
        actual_value = og.Controller.get(attribute)
        ogts.verify_values(expected_value, actual_value, _attr_error(attribute, True))
        ogts.verify_values(expected_value, db_value, _attr_error(attribute, False))

        self.assertTrue(test_node.get_attribute_exists("inputs:streaming_port"))
        attribute = test_node.get_attribute("inputs:streaming_port")
        db_value = database.inputs.streaming_port
        expected_value = 30000
        actual_value = og.Controller.get(attribute)
        ogts.verify_values(expected_value, actual_value, _attr_error(attribute, True))
        ogts.verify_values(expected_value, db_value, _attr_error(attribute, False))

        self.assertTrue(test_node.get_attribute_exists("inputs:use_system_time"))
        attribute = test_node.get_attribute("inputs:use_system_time")
        db_value = database.inputs.use_system_time
        expected_value = False
        actual_value = og.Controller.get(attribute)
        ogts.verify_values(expected_value, actual_value, _attr_error(attribute, True))
        ogts.verify_values(expected_value, db_value, _attr_error(attribute, False))
